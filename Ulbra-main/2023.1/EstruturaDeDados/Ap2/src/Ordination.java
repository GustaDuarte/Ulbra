public class Ordination {
    // Método para ordenar a lista utilizando o algoritmo Insertion Sort
    public static void insertionSort(DoublyLinkedList list) {
        list.insertionSort();
    }

    // Método para ordenar a lista utilizando o algoritmo Selection Sort
    public static void selectionSort(DoublyLinkedList list) {
        list.selectionSort();
    }
}